// Hoisting is a JavaScript mechanism that allows variables and function declarations 
// to be moved to the top of 
// their respective scopes. This means that you can declare a variable or a function 
// after you've used it in your code, and it will still work.


console.log(x);
var x = 5;  //undefined


var x;
console.log(x); //5
x = 5; 



